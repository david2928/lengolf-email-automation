export type SourceType = 'classpass' | 'resos';
export type ActionTaken = 'booking_created' | 'booking_cancelled' | 'no_slots' | 'error';

export class EmailTrackingService {
  constructor(private supabase: SupabaseClient) {}

  async isProcessed(gmailMessageId: string): Promise<boolean> {
    const { data, error } = await this.supabase
      .from('processed_emails')
      .select('id')
      .eq('gmail_message_id', gmailMessageId)
      .limit(1);
    if (error) throw new Error(`isProcessed check failed: ${error.message}`, { cause: error });
    return !!data && data.length > 0;
  }

  async markProcessed(
    gmailMessageId: string,
    sourceType: SourceType,
    actionTaken: ActionTaken,
    bookingId: string | null = null,
    errorMessage: string | null = null,
    emailMetadata: { subject?: string; date?: string } = {},
  ): Promise<void> {
    let parsedEmailDate: string | null = null;
    if (emailMetadata.date) {
      const parsed = new Date(emailMetadata.date); // RFC 2822 header date carries its own timezone
      if (!isNaN(parsed.getTime())) parsedEmailDate = parsed.toISOString();
    }

    const { error } = await this.supabase
      .from('processed_emails')
      .insert({
        gmail_message_id: gmailMessageId,
        source_type: sourceType,
        action_taken: actionTaken,
        booking_id: bookingId,
        error_message: errorMessage,
        email_subject: emailMetadata.subject || null,
        email_date: parsedEmailDate,
      });

    if (error) {
      if (error.code === '23505') {
        // Unique constraint on gmail_message_id: a concurrent invocation won the race.
        log('WARN', 'Email already marked as processed (duplicate)', { gmailMessageId, actionTaken });
        return;
      }
      throw new Error(`markProcessed failed: ${error.message}`, { cause: error });
    }
    log('INFO', 'Email marked as processed', { gmailMessageId, sourceType, actionTaken, bookingId });
  }
}
