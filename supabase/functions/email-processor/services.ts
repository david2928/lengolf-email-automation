const BAYS = {
  SOCIAL: ['Bay 2', 'Bay 3', 'Bay 1'], // Preference order (Bay 1 is Bar)
  AI: ['Bay 4'],
};
const MAX_PEOPLE_SOCIAL = 5;
const DEFAULT_GUEST_USER_ID = '0eb32c8b-b2eb-4c8d-ba19-fc4f8e15f4c7';

export interface CreateBookingInput {
  customerId?: string | null;
  customerName: string;
  customerPhone?: string | null;
  customerEmail?: string | null;
  date: string;
  startTime: string;
  duration: number;
  numberOfPeople: number;
  bay?: string | null;
  customerContactedVia?: string;
  reservationKey?: string | null;
  customerNotes?: string | null;
  isNewCustomer?: boolean;
}

export class BookingService {
  constructor(private supabase: SupabaseClient) {}

  parseTimeToStandard(timeString: string): string {
    return parseTimeToStandard(timeString);
  }

  async isBayAvailable(bay: string, date: string, startTime: string, duration: number): Promise<boolean> {
    const endTime = calculateEndTime24(startTime, duration);

    const { data, error } = await this.supabase
      .from('bookings')
      .select('id, start_time, duration')
      .eq('bay', bay)
      .eq('date', date)
      .eq('status', 'confirmed');
    if (error) throw new Error(`isBayAvailable query failed: ${error.message}`, { cause: error });

    for (const booking of data || []) {
      const existingStart = booking.start_time.slice(0, 5);
      const existingEnd = calculateEndTime24(existingStart, booking.duration);
      const hasOverlap = (
        (startTime >= existingStart && startTime < existingEnd) ||
        (endTime > existingStart && endTime <= existingEnd) ||
        (startTime <= existingStart && endTime >= existingEnd)
      );
      if (hasOverlap) return false;
    }
    return true;
  }

  async assignBay(numberOfPeople: number, date: string, startTime: string, duration: number): Promise<string | null> {
    let bayPreferences: string[];
    if (numberOfPeople === 1) {
      bayPreferences = [...BAYS.AI, ...BAYS.SOCIAL];
    } else if (numberOfPeople === 2) {
      bayPreferences = [...BAYS.SOCIAL, ...BAYS.AI];
    } else if (numberOfPeople <= MAX_PEOPLE_SOCIAL) {
      bayPreferences = BAYS.SOCIAL;
    } else {
      log('WARN', 'Party size exceeds maximum capacity', { numberOfPeople });
      return null;
    }

    for (const bay of bayPreferences) {
      if (await this.isBayAvailable(bay, date, startTime, duration)) {
        return bay;
      }
    }
    log('WARN', 'No bays available for requested time slot', { date, startTime, duration, numberOfPeople });
    return null;
  }

  async checkAvailability(date: string, startTime: string, duration: number, numberOfPeople: number):
    Promise<{ available: boolean; bay: string | null }> {
    const standardTime = parseTimeToStandard(startTime);
    const bay = await this.assignBay(numberOfPeople, date, standardTime, duration);
    return { available: bay !== null, bay };
  }

  generateBookingId(date: string): string {
    // Format: BK + YYMMDD + 4 random uppercase alphanumeric characters (matches lengolf-forms)
    const dateStr = date.replace(/-/g, '').substring(2);
    const randomSuffix = Math.random().toString(36).substring(2, 6).toUpperCase();
    return `BK${dateStr}${randomSuffix}`;
  }

  async hasBookingHistory(customerId: string): Promise<boolean> {
    const { count, error } = await this.supabase
      .from('bookings')
      .select('id', { count: 'exact', head: true })
      .eq('customer_id', customerId)
      .neq('status', 'cancelled');
    if (error) {
      log('WARN', 'Failed to check booking history, defaulting to existing customer', {
        customerId,
        error: error.message,
      });
      return true; // fail-closed: assume existing customer on error
    }
    return (count ?? 0) > 0;
  }

  async createBooking(input: CreateBookingInput): Promise<Row> {
    const {
      customerId, customerName, customerPhone, customerEmail,
      date, startTime, duration, numberOfPeople, bay,
      customerContactedVia, reservationKey = null, customerNotes = null,
      isNewCustomer = false,
    } = input;

    if (!customerName || (!customerPhone && !customerEmail) || !date || !startTime || !duration || !numberOfPeople) {
      throw new Error('Missing required booking fields');
    }

    // Generate dummy phone if missing (format: 0000 + MMDD + random 4 digits)
    let finalPhoneNumber = customerPhone;
    if (!finalPhoneNumber) {
      const today = new Date();
      const mmdd = String(today.getUTCMonth() + 1).padStart(2, '0') + String(today.getUTCDate()).padStart(2, '0');
      const random = String(Math.floor(Math.random() * 10000)).padStart(4, '0');
      finalPhoneNumber = `0000${mmdd}${random}`;
    }

    const standardStartTime = parseTimeToStandard(startTime);

    let assignedBay = bay;
    if (!assignedBay) {
      assignedBay = await this.assignBay(numberOfPeople, date, standardStartTime, duration);
      if (!assignedBay) throw new Error('NO_BAY_AVAILABLE');
    } else if (!(await this.isBayAvailable(assignedBay, date, standardStartTime, duration))) {
      throw new Error(`Bay ${assignedBay} is not available for requested time`);
    }

    const newBooking = {
      id: this.generateBookingId(date),
      user_id: DEFAULT_GUEST_USER_ID,
      customer_id: customerId || null,
      name: customerName,
      phone_number: finalPhoneNumber,
      email: customerEmail || '',
      date,
      start_time: standardStartTime,
      duration,
      number_of_people: numberOfPeople,
      bay: assignedBay,
      booking_type: customerContactedVia === 'ClassPass' ? 'ClassPass' : 'Normal Bay Rate',
      status: 'confirmed',
      customer_contacted_via: customerContactedVia || 'Email Automation',
      reservation_key: reservationKey,
      customer_notes: customerNotes,
      is_new_customer: Boolean(isNewCustomer),
      updated_by_type: 'system',
      updated_by_identifier: 'Email Automation',
    };

    const { data, error } = await this.supabase
      .from('bookings')
      .insert(newBooking)
      .select()
      .single();
    if (error) throw new Error(`createBooking failed: ${error.message}`, { cause: error });

    log('INFO', 'Booking created', {
      bookingId: data.id,
      date: data.date,
      startTime: data.start_time,
      bay: data.bay,
    });
    return data;
  }

  async cancelBooking(bookingId: string, reason: string, cancelledBy = 'Email Automation'): Promise<Row> {
    const { data, error } = await this.supabase
      .from('bookings')
      .update({
        status: 'cancelled',
        cancelled_by_type: 'system',
        cancelled_by_identifier: cancelledBy,
        cancellation_reason: reason,
      })
      .eq('id', bookingId)
      .select()
      .single();
    if (error) throw new Error(`cancelBooking failed: ${error.message}`, { cause: error });
    log('INFO', 'Booking cancelled', { bookingId, reason });
    return data;
  }

  async findBookingByReservationKey(reservationKey: string): Promise<Row | null> {
    const { data, error } = await this.supabase
      .from('bookings')
      .select('*')
      .eq('reservation_key', reservationKey)
      .eq('status', 'confirmed')
      .order('created_at', { ascending: false })
      .limit(1);
    if (error) throw new Error(`findBookingByReservationKey failed: ${error.message}`, { cause: error });
    return data && data.length > 0 ? data[0] : null;
  }

  async findBookingByDetails(
    customerName: string | null,
    phone: string | null,
    email: string | null,
    date: string,
    startTime: string,
    source: string | null = null,
  ): Promise<Row | null> {
    const standardStartTime = parseTimeToStandard(startTime);

    let query = this.supabase
      .from('bookings')
      .select('*')
      .eq('status', 'confirmed')
      .eq('date', date)
      .eq('start_time', standardStartTime);
    if (source) query = query.eq('customer_contacted_via', source);

    const { data, error } = await query;
    if (error) throw new Error(`findBookingByDetails failed: ${error.message}`, { cause: error });
    if (!data || data.length === 0) return null;

    const matches = data.filter((booking: Row) => {
      const phoneMatch = phone && booking.phone_number?.includes(phone);
      const emailMatch = email && booking.email && booking.email.toLowerCase() === email.toLowerCase();
      const nameMatch = customerName && booking.name?.toLowerCase().includes(customerName.toLowerCase());
      return phoneMatch || emailMatch || nameMatch;
    });

    if (matches.length === 0) return null;
    if (matches.length > 1) {
      log('WARN', 'Multiple bookings found matching criteria - ambiguous', {
        date,
        startTime: standardStartTime,
        matchCount: matches.length,
      });
      return null;
    }
    return matches[0];
  }
}

